use super::AstVisitor;
use std::io;

pub struct NodeModulesImportsVisitor;

impl AstVisitor for NodeModulesImportsVisitor {
    fn visit_import_directive(
        &mut self,
        _source_unit: &solidity::ast::SourceUnit,
        import_directive: &solidity::ast::ImportDirective,
    ) -> io::Result<()> {
        if import_directive.file.contains("../node_modules") {
            println!("\tUnnecessary relative node_modules import:");
            println!("\t\t\"{}\"", import_directive.source_unit);
        }

        Ok(())
    }
}
